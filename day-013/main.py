# Fix the code, so that it prints out the following:
# [2, 4, 6, 10, 16, 26]

def mutate(a_list):
  b_list = []
  for item in a_list:
    new_item = item * 2
  b_list.append(new_item)
  print(b_list)

mutate([1,2,3,5,8,13])



