# Printing to the Console in Python

# using print() function

# This will print "Hello World" into the console.
# print() function always start from the beginning of the line without any space.
# Click run to execute these lines of code into the console.

print("Hello World") # We can use double quotes as well as single quotes.
print('Hello World') # Anything inside the quotes are Strings.
print(23) # Numbers don't require single or double quotes.
print(2+2) # We can do calculation inside print() function.
print("3 + 3") # Since the numbers and the operator is inside the quotes, Python will treat it as String.



