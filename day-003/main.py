# Control Flow with if / else and Conditional Operators

print("Welcome to the rollercoaster!")
height = int(input("What is your height in cm? "))

# Now we are going to check whether if the height of the user is greater than 120 cm or not

if(height >= 120):
    print("You can ride the rollercoaster!")
else:
    print("Sorry you have to grow taller before you can ride.")

# How this if/else statements work?
# To use the if/else statement: Firstly we have to the 'if' keyword
# Then we put the condition inside parentheses.
# The condition here is the 'height', which the user will input.
# Then we use the colon (:)
# When we hit enter, Python will automatically indent the next line, which is the code block for the 'if' statement.
# If the condition is true, Python will execute the 'if' block
# And if the condition is false, Python will execute the 'else' block.
# 'else' statement do not require any condition to be passed on.
# The 'else' statement is written on the next line and the indentation of 'if' and 'else' statement should be same.



