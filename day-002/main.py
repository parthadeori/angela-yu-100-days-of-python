# Mathematical Operations in Python

# Addition (+)
print("Addition")
print(2 + 3)

# Subtraction (-)
print("Subtraction")
print(34 - 23)

# Multiplication (*)
print("Multiplication")
print(2 * 4)

# Division ( / )
print("Division")
print(12 / 4) # Result displayed in float. i.e., 3.0

# Exponent or Power ( ** )
print("Exponent")
print(2 ** 3) # 2 ** 3 => 2 * 2 * 2 = 8

# Parenthese ()
print("Parentheses")
print((3 + 4) * 4) # This will print 28

# Plus Equal (+=)
print("Plus Equal(+=)")
x = 3
x += 3 # This means x + 3
print(x) # This will print x + 3 = 3 + 3 = 6

# Minus Equal (-=)
print("Minus Equal (-=)")
y = 4
y -= 6
print(y) # y - 6 = 4 - 6 = -2

# Same goes with (*=) and (/=)

# Remainder (%)
print("Remainder (%)")
print(8 % 2) # Remainder will be 0
print(10 % 3) # Remainder will be 1

# Quotient (//) or Floor Division (see 07-rounding-off.py file)
print("Quotient (//)")
print(24 // 12) # The Quotient will be 2 because 12 * 2 = 24, where 2 is the Quotient.

# OPERATORS PRECEDENCE

# The order of precedence in Python is similar to PEMDAS or BIDMAS rule which stands for

# Parentheses > Exponent > Multiplication > Division > Addition > Subtraction
# Brackets > Indices(Exponent) > Division > Multiplication > Addition > Subtraction 



